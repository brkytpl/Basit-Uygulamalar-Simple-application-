class Matematik {
  static const pi = 3.14;

  static topla(double sayi1, double sayi2) {
    return sayi1 + sayi2;
  }

  static cikarma(double sayi1, double sayi2) {
    return sayi1 - sayi2;
  }

  static carpma(double sayi1, double sayi2) {
    return sayi1 * sayi2;
  }

  static bolme(double sayi1, double sayi2) {
    return sayi1 / sayi2;
  }
}
